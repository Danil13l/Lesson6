public class GameEntity {
    private int hils;
    private int damag;

    public int getHils() {
        return hils;
    }

    public void setHils(int hils) {
        this.hils = hils;
    }

    public int getDamag() {
        return damag;
    }

    public void setDamag(int damag) {
        this.damag = damag;
    }
}
